    private void populateAccommodationComboBox() {
        String[] accommodationsDetail = app.getUnpaidAccommodationsDetail(unpaidAccommodations);
        accommodationComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(accommodationsDetail));

        if (accommodationComboBox.getModel().getSize() > 0) {
            accommodationComboBox.setSelectedIndex(0);
        }
    }

    private void populateServiceComboBox() {
        String[] services = app.getServicesDescription();
        serviceComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(services));

        if (serviceComboBox.getModel().getSize() > 0) {
            serviceComboBox.setSelectedIndex(0);
        }
    }

