    private void registerBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerBtnMouseReleased
        int selectedAccommodationIndex = accommodationComboBox.getSelectedIndex();
        int selectedServiceIndex = serviceComboBox.getSelectedIndex();

        if (selectedAccommodationIndex < 0 || selectedServiceIndex < 0) {
            return;
        }

        Accommodation accommodation = unpaidAccommodations.get(selectedAccommodationIndex);
        Service service = app.getSelectedService(selectedServiceIndex);
        Date date = dateChooser.getDate();

        accommodation.addService(new Service(service.getPrice(), service.getDescription(), date));
        app.saveAccommodations();
        JOptionPane.showMessageDialog(null, "Služba bola zaznamenaná.");
    }//GEN-LAST:event_registerBtnMouseReleased

    private void serviceComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serviceComboBoxActionPerformed
        int index = serviceComboBox.getSelectedIndex();
        double price = app.getSelectedService(index).getPrice();

        priceFld.setText(new DecimalFormat("########0.00").format(price));
    }//GEN-LAST:event_serviceComboBoxActionPerformed

    private void accommodationComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accommodationComboBoxActionPerformed
        int selectedAccommodationIndex = accommodationComboBox.getSelectedIndex();

        if (selectedAccommodationIndex < 0) {
            return;
        }

        Accommodation accommodation = unpaidAccommodations.get(selectedAccommodationIndex);
        roomFld.setText(accommodation.getRoom().getId());
        fromFld.setText(Tool.dateToString(accommodation.getFrom()));
        untilFld.setText(Tool.dateToString(accommodation.getUntil()));

    }//GEN-LAST:event_accommodationComboBoxActionPerformed

