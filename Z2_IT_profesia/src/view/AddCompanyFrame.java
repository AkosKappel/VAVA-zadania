    private void addBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseReleased
        if (nameField.getText().isBlank() || businessField.getText().isBlank()
                || countField.getText().isBlank() || logoField.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Prosím vyplňte všetky informácie!", "Chyba", JOptionPane.ERROR_MESSAGE);
        } else {
            String name = nameField.getText().trim();
            String business = businessField.getText().trim();
            int count;
            String path = logoField.getText();
            ImageIcon logo = new ImageIcon(path);

            try {
                count = Integer.parseInt(countField.getText().trim());
                if (count < 0) {
                    throw new NumberFormatException();
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Počet zamestnancov musí byť kladná číselná hodnota!", "Chyba", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Company company = new Company(name, business, count, logo);
            companies.add(company);
            DataSerializer.saveCompaniesToFile(companies, DataSerializer.COMPANIES_FILE);

            JOptionPane.showMessageDialog(null, "Údaje o novom programátorovi boli úspešne uložené!");
            dispose();
            parentFrame.setVisible(true);
        }
    }//GEN-LAST:event_addBtnMouseReleased

