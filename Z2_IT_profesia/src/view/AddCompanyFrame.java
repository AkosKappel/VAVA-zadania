    private void selectBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selectBtnMouseReleased
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        chooser.showOpenDialog(this);

        File f = chooser.getSelectedFile();
        if (f == null) {
            return;
        }

        if (f.getName().endsWith(".jpg") || f.getName().endsWith(".png")) {
            String path = f.getAbsolutePath();
            logoField.setText(path);
            ImageIcon logo = new ImageIcon(path);

            Image img = logo.getImage();
            Image imgScale = img.getScaledInstance(logoImageLabel.getWidth(), logoImageLabel.getHeight(), Image.SCALE_SMOOTH);
            logoImageLabel.setIcon(new ImageIcon(imgScale));
        } else {
            JOptionPane.showMessageDialog(null, "Prosím vyberte súbor typu jpg alebo png.", "Chyba", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_selectBtnMouseReleased

